class PerimeterOfCircle 
{
	public static void main(String[] args) 
	{

      float area = 22/7f * (5.5f*5.5f); // Calculating area of circle

	  float perimeter = 2f * 22/7f * 5.5f; // Calculating perimeter of circle

	  System.out.println("Area of Circle: "+area+" and Perimeter of Circle: "+perimeter);
	}
}
