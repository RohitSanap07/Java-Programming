class DoWhilePractice 
{
	public static void main(String[] args) 
	{

        int grt = 1; // (Initilization) greeting with 1   

		do
		{ 
          System.out.println("Good Morning");
		  grt ++; // (Update) greeting
			  
		}
		while (grt<=5); // (Condition) greeting goes upto 5 times  
		
   }
}
